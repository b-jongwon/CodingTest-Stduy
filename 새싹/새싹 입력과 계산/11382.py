a,b,c=list(map(int,input().split()))
print(a+b+c)
#input(): 사용자로부터 한 줄의 문자열을 입력받습니다. (예: "10 20 30")
#.split(): 입력받은 문자열을 공백 기준으로 쪼개서 리스트로 만듭니다.  (결과: ['10', '20', '30'])
#map(int, ...): 리스트의 각 문자열 요소를 정수(int)로 형변환합니다. 
#list(...): map 객체를 실제 리스트 형태로 변환합니다. (결과: [10, 20, 30])
#언패킹(Unpacking): 리스트 안에 든 3개의 값을 변수 a, b, c에 각각 순서대로 할당합니다.